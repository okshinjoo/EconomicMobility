  "May 2025 BLS pay data · 2024–34 BLS employment projections";

/** Growth band in plain words (BLS-style framing, ~4% = all-job average). */
export function growthLabel(g: number): string {
  if (g >= 9) return "Much faster than average";
  if (g >= 5) return "Faster than average";
  if (g >= 2) return "About average";
  if (g >= -1) return "Little change";
  return "Declining";
}

